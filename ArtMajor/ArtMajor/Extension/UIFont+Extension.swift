import UIKit

extension UIFont {
    
    var plain: UIFont {
        return UIFont(name: "S-CoreDream-3Light", size: 14)!
    }
    
    var bold: UIFont {
        return UIFont(name: "S-CoreDream-6Bold", size: 18)!
    }

    var enforcement: UIFont {
        return UIFont(name: "S-CoreDream-7ExtraBold", size: 22)!
    }
    
}
